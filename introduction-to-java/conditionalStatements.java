public class conditionalStatements {
    public static void main(String[] args) {
        int myInt = 0;

        if (myInt < 7) {
            System.out.println("Given integer is less than 7.");
        } else if (myInt == 7) {
            System.out.println("Given integer is equal to 7.");
        } else {
            System.out.println("Given integer is greater than 7.");
        }
    }
}
