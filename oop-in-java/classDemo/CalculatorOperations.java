public class CalculatorOperations {
    public int mySummation(int myInteger1, int myInteger2) {
        return myInteger1 + myInteger2;
    }

    public int mySubstraction(int myInteger1, int myInteger2) {
        return myInteger1 - myInteger2;
    }

    public int myMultiplication(int myInteger1, int myInteger2) {
        return myInteger1 * myInteger2;
    }

    public int myDivision(int myInteger1, int myInteger2) {
        return myInteger1 / myInteger2;
    }

}
